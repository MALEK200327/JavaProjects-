public class Radius {
	public static void main (String [] args)  {
		final double PI = 22/7;
		int radius = 5;
		double cicumference = (2.0*radius)*PI;
		double area = PI*Math.pow(radius,2.0);
		System.out.println(" The radius is " + radius + " The circumfrence is " + cicumference + " The area is equal to " + area);
	} 
}
		